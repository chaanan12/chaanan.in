<p class="font-24 margin-vertical-20 font-italic" style="text-align:justify;">As the largest private outpatient assessment and treatment facility specifically for Autism and related Neurodevelopmental Disorders in Connecticut, Chaanan maintains an interdisciplinary clinical staff comprised of psychologists, behavior analysts, and speech and occupational therapists, providing evaluation and treatment services across the lifespan for families and individuals. </p>
<p class="font-24 margin-vertical-20 font-italic" style="text-align:justify;">>Beyond direct clinical care, Chaanan has been deeply involved in helping educational programs at the local, state and regional level meet the diverse learning needs of students with ASD and other complex challenges. Through an active program of consultative and training services, Chaanan provides program evaluation, development, training, and consultative support to educational, governmental, and private organizations supporting those families and their children. </p>
<p class="font-24 margin-vertical-20 font-italic" style="text-align:justify;">Through the years as the team at Chaanan grew, the practice moved into progressively larger facilities in Mansfield and Tolland before settling into Glastonbury in 2003. In 2012 Chaanan again expanded into a second location in Glastonbury and now occupies over 15,000 sq ft with spaces dedicated to clinical treatment and consultation, as well as telehealth.</p>
<p class="font-24 margin-vertical-20 font-italic" style="text-align:justify;">In 2013, Chaanan established The Center for Independence to provide a more specialized entity for evaluation and treatment of later adolescents and adults as well as consultation and therapy for specific problems such as feeding, sleep, and elimination disorders. </p>
<p class="font-24 margin-vertical-20 font-italic" style="text-align:justify;">As Chaanan has grown, its clinical and consultative impact has expanded throughout the region and abroad. To support this growth, in 2016 Chaanan reorganized its clinical services into four clearly-defined Divisions: <span class="font-italic"><strong>Chaanan Assocition for MR Children,Jalandhar,Punjab,144003(SR/Company Act Public Charitable Trust:331)</strong>.</span> Within each of these Divisions, program development is ongoing as the need for specialized solutions for families, individuals, and organizations arises. </p>
<p class="font-24 margin-vertical-20 font-italic" style="text-align:justify;">This divisional breakout is designed to address the whole spectrum of clinical and educational needs that an individual with ASD may require from initial diagnosis and treatment to programmatic and educational support to transition planning, vocational support, and resources to facilitate independent living.</p>
<p class="font-24 margin-vertical-20 font-italic" style="text-align:justify;">Recognizing that ASD is a global issue, Chaanan formally established an International Division with the explicit mission to develop scalable services and resources to support individuals with ASD, their families, and their communities around the world. The International Division draws upon the clinical, telehealth, and global public health expertise of the entire faculty at Chaanan to provide services to individual children and their families, hospitals, public agencies, and non-governmental organizations (NGOs) throughout the world. Recent projects have been developed in the India, Norway, Finland, Italy, Kazakhstan, Peru, Sri Lanka, Switzerland, and the Turks and Caicos Islands.</p>

