<div id="accordions-show-description-2-group-1" class="panel-collapse collapse">
<div class="panel-body">
<p><strong>Description:</strong></p>
<p>Given the importance of reading to both academic success and successful functioning in the community, reading interventions that are specifically tailored to students with Autism Spectrum Disorders are critical. This workshop is designed to develop greater competency in addressing the learning profiles of students within this population who have mastered the techniques of decoding text, but struggle with comprehension.</p>
<p>This dynamic learning opportunity will include discussion of research-based methodologies to improve student learning within the broad curriculum domain of reading comprehension skills. More specifically, the essential building blocks of successful reading comprehension will be reviewed including vocabulary, background information, literal comprehension, inferencing, main idea, and the application of cognitive strategies to support reading comprehension. Lesson plans for component reading comprehension skills based on the principles of behavior analysis will be reviewed and participants will have the opportunity to implement plans targeting specific skills in this domain. Participants will have the opportunity to practice data collection and analysis to inform instruction and learn about potential accommodations and modifications for specific learning profiles.</p>
<p><strong>Learning Objectives:</strong></p>
<ul class="fa-ul">
<li><i class="fa fa-check fa-li accent"></i>Participants will be able to identify the building blocks of reading comprehension.</li>
<li><i class="fa fa-check fa-li accent"></i>Participants will be able to read a lesson plan outlining steps for teaching reading comprehension skills.</li>
<li><i class="fa fa-check fa-li accent"></i>Participants will understand the sequential steps that lead to target skill acquisition.</li>
<li><i class="fa fa-check fa-li accent"></i>Participants will be able to implement steps within a lesson plan that addresses a component of reading comprehension (e.g., main idea).</li>
<li><i class="fa fa-check fa-li accent"></i>Participants will be able to implement prompting hierarchies for steps within their identified sequence.</li>
<li><i class="fa fa-check fa-li accent"></i>Participants will be able to utilize data collection protocols to inform decision making.</li>
<li><i class="fa fa-check fa-li accent"></i>Participants will identify potential accommodations and modifications to lesson plans.</li>
</ul>
