<h3>Workshop for Teaching Reading Comprehension to Students with Autism Spectrum Disorders.</h3>
<p>Date: <strong class="accent6">August 20th 2018</strong> <span class="margin-datetime">Time: <strong class="accent6">9:00-4:30</strong></span></p>
<div id="accordions-show-description-2" class="panel-group accordions-list custom-accordion">
<div class="panel panel-default">
<div class="panel-heading">
<h4 class="panel-title"><a class="accordion-toggle collapsed" data-toggle="collapse" data-parent="#accordions-show-description-2" href="#accordions-show-description-2-group-1">Show Description</a></h4>
