<p><strong>Description:</strong></p>
<p>Students who have complex needs, including cognitive, academic, motor, social-emotional, and/or behavioral impairments, often present as puzzles to educators. Conducting assessments that are informative and will guide instruction, as well as delivering programming that appropriately meets the needs of these learners can present with challenges to even the most invested of educators.</p>
<p>This workshop is designed to develop greater competency in assessing and addressing the needs of students with complex needs in the cognitive and academic domains.</p>
<p><strong>Participants will learn how to:</strong></p>
<ul class="fa-ul">
<li><i class="fa fa-check fa-li accent"></i>Choose what is important to assess within the targeted domain</li>
<li><i class="fa fa-check fa-li accent"></i>Select appropriate evaluation instruments</li>
<li><i class="fa fa-check fa-li accent"></i>Modify assessment, as necessary, to capture the important information</li>
<li><i class="fa fa-check fa-li accent"></i>Use the information obtained to drive instruction and intervention</li>
</ul>
<p>The day will consist of dynamic learning opportunities including discussion of research-based methodologies to choose and modify assessments to better understand learner’s areas of strength and weakness. Participants will have the opportunity to practice choosing and modifying assessments, using assessment results to drive instructional and intervention targets, and modifying instruction/intervention based on assessment.</p>
